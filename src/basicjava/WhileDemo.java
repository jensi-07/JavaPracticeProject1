package basicjava;

public class WhileDemo {

	public static void main(String[] args) {
		//While loop
	
	//basic syntax: while(boolean)
//		1.initialize method: create variable int i = ...
//		2.condition: put a condition
//		3.increment/decrement
	
	int i = 0;
	while(i<10) {
		System.out.println(i);
		i++;
	}
	int j = 10;
	while(j>=-10) {
		System.out.println(j);
		j--;
	}
	
	
	
	}

}
